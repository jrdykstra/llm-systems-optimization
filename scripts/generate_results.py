"""
Generate canonical results for the project.

Runs configured (model, task_type) combinations, grades them, and writes
a markdown summary to results/RESULTS.md. Skips runs whose predictions
already exist on disk unless --force is passed.

Usage:
    python scripts/generate_results.py
    python scripts/generate_results.py --force
"""

from __future__ import annotations

import argparse
import subprocess
import sys
from pathlib import Path
from typing import Dict, List

ROOT = Path(__file__).resolve().parent.parent
RUNS_DIR = ROOT / "runs"
RESULTS_DIR = ROOT / "results"
RESULTS_FILE = RESULTS_DIR / "RESULTS.md"

sys.path.insert(0, str(ROOT))

from src.utils import load_jsonl  # noqa: E402
from src.summarize_runs import summarize_one  # noqa: E402


CHEAP_MODEL = "gpt-4o-mini"
EXPENSIVE_MODEL = "gpt-4o"

RUNS: Dict[str, List[dict]] = {
    "extraction_v1": [
        {
            "label": EXPENSIVE_MODEL,
            "pred_file": f"{EXPENSIVE_MODEL}_extraction_v1_predictions.jsonl",
            "run_cmd": ["src.run_model", "--provider", "openai",
                        "--model", EXPENSIVE_MODEL, "--task-type", "extraction_v1"],
        },
        {
            "label": CHEAP_MODEL,
            "pred_file": f"{CHEAP_MODEL}_extraction_v1_predictions.jsonl",
            "run_cmd": ["src.run_model", "--provider", "openai",
                        "--model", CHEAP_MODEL, "--task-type", "extraction_v1"],
        },
        {
            "label": "cascade",
            "pred_file": f"cascade_{CHEAP_MODEL}_{EXPENSIVE_MODEL}_extraction_v1_predictions.jsonl",
            "run_cmd": ["src.run_cascade", "--task-type", "extraction_v1"],
        },
    ],
    "antitrust_v1": [
        {
            "label": EXPENSIVE_MODEL,
            "pred_file": f"{EXPENSIVE_MODEL}_antitrust_v1_predictions.jsonl",
            "run_cmd": ["src.run_model", "--provider", "openai",
                        "--model", EXPENSIVE_MODEL, "--task-type", "antitrust_v1"],
        },
        {
            "label": CHEAP_MODEL,
            "pred_file": f"{CHEAP_MODEL}_antitrust_v1_predictions.jsonl",
            "run_cmd": ["src.run_model", "--provider", "openai",
                        "--model", CHEAP_MODEL, "--task-type", "antitrust_v1"],
        },
        {
            "label": "cascade",
            "pred_file": f"cascade_{CHEAP_MODEL}_{EXPENSIVE_MODEL}_antitrust_v1_predictions.jsonl",
            "run_cmd": ["src.run_cascade", "--task-type", "antitrust_v1"],
        },
    ],
}


def run_module(args: List[str]) -> None:
    """Invoke `python -m <args>` from the project root. Exit on failure."""
    cmd = [sys.executable, "-m"] + args
    result = subprocess.run(cmd, cwd=ROOT)
    if result.returncode != 0:
        print(f"  ✗ FAILED: python -m {' '.join(args)}", file=sys.stderr)
        sys.exit(1)


def run_predictions_if_needed(pred_file: str, run_cmd: List[str], force: bool) -> None:
    pred_path = RUNS_DIR / pred_file
    if pred_path.exists() and not force:
        print(f"  - skipping (exists): {pred_file}")
        return
    print(f"  > running: python -m {' '.join(run_cmd)}")
    run_module(run_cmd)


def grade(pred_file: str, task_type: str) -> Path:
    grades_file = pred_file.replace("_predictions.jsonl", "_grades.jsonl")
    grades_path = RUNS_DIR / grades_file
    cmd = [
        "src.grade_predictions",
        "--predictions", str(RUNS_DIR / pred_file),
        "--task-type", task_type,
        "--output", str(grades_path),
    ]
    print(f"  > grading: {pred_file}")
    run_module(cmd)
    return grades_path


def build_markdown_table(summaries: List[dict]) -> str:
    headers = ["Model", "N", "Mean Score", "Perfect", "Schema Valid",
               "Total Cost (USD)", "Cost/Correct"]
    lines = ["| " + " | ".join(headers) + " |"]
    lines.append("|" + "|".join(["---"] * len(headers)) + "|")
    for s in summaries:
        cost = f"${s['total_cost_usd']:.6f}" if s["total_cost_usd"] is not None else "n/a"
        cpc = (f"${s['cost_per_correct_usd']:.6f}"
               if s["cost_per_correct_usd"] is not None else "n/a")
        lines.append(
            f"| {s['model']} | {s['n']} | {s['mean_score']:.4f} | "
            f"{s['perfect']}/{s['n']} | {s['schema_valid']}/{s['n']} | "
            f"{cost} | {cpc} |"
        )
    return "\n".join(lines)


def parse_args() -> argparse.Namespace:
    parser = argparse.ArgumentParser(description=__doc__)
    parser.add_argument(
        "--force",
        action="store_true",
        help="Re-run all models even if prediction files already exist.",
    )
    return parser.parse_args()


def main() -> None:
    args = parse_args()
    RESULTS_DIR.mkdir(parents=True, exist_ok=True)

    sections: List[str] = [
        "# Benchmark Results\n",
        "Auto-generated by `scripts/generate_results.py`. "
        "Run that script to reproduce.\n",
    ]

    for task_type, configs in RUNS.items():
        print(f"\n=== {task_type} ===")

        # Step 1: ensure all predictions exist
        for cfg in configs:
            run_predictions_if_needed(cfg["pred_file"], cfg["run_cmd"], args.force)

        # Step 2: grade each one and collect summaries
        summaries: List[dict] = []
        for cfg in configs:
            grades_path = grade(cfg["pred_file"], task_type)
            rows = load_jsonl(str(grades_path))
            summary = summarize_one(rows)
            if summary is None:
                continue
            # Override the auto-detected model label with our config label
            summary["model"] = cfg["label"]
            summaries.append(summary)

        # Step 3: build markdown section
        sections.append(f"## {task_type}\n")
        sections.append(build_markdown_table(summaries))
        sections.append("")

    output = "\n".join(sections) + "\n"
    RESULTS_FILE.write_text(output, encoding="utf-8")
    print(f"\nWrote {RESULTS_FILE}")
    print("\n" + output)


if __name__ == "__main__":
    main()
